function normalizeSpacing(text: string) {
  return text
    .replace(/\s+/g, " ")
    .replace(/\s+([,.;!?])/g, "$1")
    .replace(/([,.;!?])(?=\S)/g, "$1 ")
    .replace(/\(\s+/g, "(")
    .replace(/\s+\)/g, ")")
    .replace(/\[\s+/g, "[")
    .replace(/\s+\]/g, "]")
    .trim();
}

export function normalizeExplanationText(text: string) {
  let normalized = normalizeSpacing(text);

  for (const [from, to] of COMMON_EXPLANATION_FIXES) {
    normalized = normalized.replaceAll(from, to);
  }

  return normalizeSpacing(normalized);
}

export function formatExplanationSteps(steps: string[]) {
  return steps.flatMap((step) => {
    const normalized = normalizeExplanationText(step);

    return normalized
      .split(/(?<=[.!?])\s+(?=[A-ZА-Я(])/u)
      .map((item) => item.trim())
      .filter(Boolean);
  });
}
